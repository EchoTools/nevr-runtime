# HashA function (?HashA@SMatSymData@NRadEngine@@SA_K_K0@Z at 0x140107fd0)
def hash_a(base_hash, name_hash):
    """Combines base_hash with name_hash using byte_lut table"""
    mask = 0xFFFFFFFFFFFFFFFF
    
    # Extract 8 bytes from base_hash
    parts = []
    for i in range(8):
        byte_val = (base_hash >> (i * 8)) & 0xFF
        # Apply byte mask lookup and AND with name_hash
